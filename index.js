let entrada = parseInt(prompt("Ingresa un numero del 1 al 5"));

while (entrada != 3) {
  switch (entrada) {
    case 1:
      break;

    case 2:
      break;

    case 4:
      break;

    case 5:
      break;

    default:
      alert("Ingrese un numero dentro del rango 1 a 5 ");
      break;
  }

  entrada = parseInt(prompt("Ingresar otro dato"));
}
